    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
       
        if ( (id.getText()).equals("") == false )
        {
            try
            {
                Connection con = (Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/RAS","root","go5a1on1");
                Statement st = con.createStatement();

                String query1 = "Select * from DISH where ID = \"" + id.getText() +  "\"; ";
                ResultSet rs=st.executeQuery(query1);
                                
                if ( !rs.isBeforeFirst() )
                {
                    JOptionPane.showMessageDialog(null,"ITEM with this ID ABSENT !! ","Modify Failed",JOptionPane.ERROR_MESSAGE);
                    id.setText(null);
                }
                else
                {    
                    rs.next() ; 
                    modify.setVisible(true) ; 
                    
                    name.setText(rs.getString("name")) ; 
                    cuisine.setText(rs.getString("cuisine")) ; 
                    sp.setText(""+rs.getInt("sp")+"") ; 
                    mp.setText(""+rs.getInt("mp")+"") ; 
                    
                    Ing1.setText(rs.getString("I1")) ; 
                    Q1.setText(""+rs.getInt("Q1")+"") ; 
                    
                    Ing2.setText(rs.getString("I2")) ; 
                    Q2.setText(""+rs.getInt("Q2")+"") ;  
                    
                    Ing3.setText(rs.getString("I3")) ; 
                    Q3.setText(""+rs.getInt("Q3")+"") ; 
                    
                    Ing4.setText(rs.getString("I4")) ; 
                    Q4.setText(""+rs.getInt("Q4")+"") ;   
                    
                }
            }
            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(null,ex,"Alert",JOptionPane.ERROR_MESSAGE);                 
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "ENTER ID !! ","Alert",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_searchActionPerformed

