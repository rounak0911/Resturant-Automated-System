         s = id.getText()      ; if ( s.equals("") ) return 0 ; 
         s = cuisine.getText() ; if ( s.equals("") ) return 0 ; 
         s = sp.getText()      ; if ( s.equals("") ) return 0 ;     
         s = mp.getText()      ; if ( s.equals("") ) return 0 ; 
         
         s = Ing1.getText() ; s1 = Q1.getText() ; if ( !s.equals("") && s1.equals("") ) return 1 ;   
         s = Ing2.getText() ; s1 = Q2.getText() ; if ( !s.equals("") && s1.equals("") ) return 1 ;           
         s = Ing3.getText() ; s1 = Q3.getText() ; if ( !s.equals("") && s1.equals("") ) return 1 ;           
         s = Ing4.getText() ; s1 = Q4.getText() ; if ( !s.equals("") && s1.equals("") ) return 1 ;  
         
         return -1 ; 
          
